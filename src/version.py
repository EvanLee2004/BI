def changelog() -> list[dict]:
    """返回更新日志副本（防调用方误改常量）。"""
    return [dict(e, items=list(e.get("items", []))) for e in PRODUCT_CHANGELOG]


def version_info() -> dict:
    """给 /api/version 下发的结构：版本号 + 阶段 + 标签 + 更新日志。"""
    v = read_version()
    return {
        "version": v,
        "stage": product_stage(v),
        "label": product_label(v),
        "changelog": changelog(),
    }
